namespace MAPS.API.Services.Chatbot;

public class SafetyGuardResult
{
    public string  FilteredResponse        { get; set; } = string.Empty;
    public bool    WasRedirectedToProtocol { get; set; }
    public bool    PrescriptionBlocked     { get; set; }
    public string  Disclaimer              { get; set; } = string.Empty;
    public float   ContextConfidence       { get; set; }
}

public interface ISafetyGuardModule
{
    SafetyGuardResult Apply(string rawResponse, float contextConfidence);
}

public class SafetyGuardModule : ISafetyGuardModule
{
    private const string StandardDisclaimer =
        "\n\n⚠️ **AI Disclaimer**: This response is generated by the MAPS AI assistant " +
        "for clinical decision support only. It must be validated by your clinical " +
        "judgment before any action. Do not act on AI suggestions without " +
        "professional assessment.";

    // Phrases that indicate direct prescription recommendations
    private static readonly string[] PrescriptionPhrases =
    {
        "prescribe ", "i recommend prescribing", "you should prescribe",
        "administer ", "give the patient", "dose of ", "mg daily",
        "mg twice", "mg three times", "start the patient on"
    };

    // Emergency condition keywords
    private static readonly string[] EmergencyKeywords =
    {
        "cardiac arrest", "myocardial infarction", "stroke", "anaphylaxis",
        "respiratory failure", "septic shock", "code blue", "resuscitation",
        "life-threatening", "immediately call", "call 911", "call 999"
    };

    // Out-of-domain content to block
    private static readonly string[] BlockedTopics =
    {
        "how to hack", "illegal", "weapon", "suicide method",
        "drug synthesis", "bypass security"
    };

    public SafetyGuardResult Apply(string rawResponse, float contextConfidence)
    {
        var response            = rawResponse;
        var wasRedirected       = false;
        var prescriptionBlocked = false;

        // ── 1. Block out-of-domain content ────────────────────────────────────
        var lowerResponse = response.ToLower();
        foreach (var blocked in BlockedTopics)
        {
            if (lowerResponse.Contains(blocked))
            {
                response = "I can only assist with clinical decision support questions. " +
                           "Please ask about patient symptoms, diagnoses, medications, " +
                           "or clinical guidelines.";
                wasRedirected = true;
                break;
            }
        }

        // ── 2. Emergency redirect ──────────────────────────────────────────────
        if (!wasRedirected)
        {
            foreach (var keyword in EmergencyKeywords)
            {
                if (lowerResponse.Contains(keyword))
                {
                    response = $"⚠️ **Emergency Protocol**: {response}\n\n" +
                               "**IMMEDIATE ACTION REQUIRED**: Follow your institution's " +
                               "emergency protocols. Contact the emergency response team. " +
                               "Do not rely on AI suggestions in emergency situations — " +
                               "follow established clinical guidelines.";
                    wasRedirected = true;
                    break;
                }
            }
        }

        // ── 3. Prescription filter ────────────────────────────────────────────
        if (!wasRedirected)
        {
            bool hasPrescriptionContent = PrescriptionPhrases
                .Any(p => lowerResponse.Contains(p));

            if (hasPrescriptionContent)
            {
                // Replace direct prescription advice with guidance
                response = response + "\n\n📋 **Prescribing Note**: The above contains " +
                           "medication-related information for reference only. " +
                           "All prescribing decisions must be made by the treating " +
                           "physician using full clinical context, current formulary, " +
                           "and patient-specific factors. Consult pharmacological " +
                           "references and drug interaction databases before prescribing.";
                prescriptionBlocked = true;
            }
        }

        // ── 4. Low confidence tag ─────────────────────────────────────────────
        string confidenceTag = contextConfidence switch
        {
            >= 0.8f => "🟢 High context relevance",
            >= 0.5f => "🟡 Moderate context relevance",
            _       => "🔴 Low context relevance — response based on general training data only"
        };

        // ── 5. Inject disclaimer ──────────────────────────────────────────────
        var disclaimer = StandardDisclaimer +
                         $"\n\n📊 **Context**: {confidenceTag}";

        return new SafetyGuardResult
        {
            FilteredResponse        = response,
            WasRedirectedToProtocol = wasRedirected,
            PrescriptionBlocked     = prescriptionBlocked,
            Disclaimer              = disclaimer,
            ContextConfidence       = contextConfidence
        };
    }
}
